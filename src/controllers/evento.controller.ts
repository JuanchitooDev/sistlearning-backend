import { Request, Response } from 'express'
import EventoService from '../services/evento.service'

class EventoController {
    async getEventos(req: Request, res: Response) {
        const response = await EventoService.getEventos()
        if (response.result) {
            res.status(200).json(response)
        } else {
            res.status(500).json(response)
            // res.status(500).json({ message: response.error || 'Error al obtener los actos médicos' });
        }
    }

    async getEventoById(req: Request, res: Response) {
        const response = await EventoService.getEventoById(+req.params.id)
        if (response.result) {
            if (response.data) {
                res.status(200).json(response)
            } else {
                res.status(404).json({ message: 'Evento no encontrado' });
            }
        } else {
            res.status(500).json(response)
            // res.status(500).json({ message: response.error || 'Error al obtener el acto médico' });
        }
    }

    async createEvento(req: Request, res: Response) {
        const response = await EventoService.createEvento(req.body);
        if (response.result) {
            res.status(201).json(response);
        } else {
            res.status(500).json(response);
            // res.status(500).json({ message: response.error || 'Error al crear el acto médico' });
        }
    }

    async updateEvento(req: Request, res: Response) {
        const { id } = req.params;
        const response = await EventoService.updateEvento(+id, req.body);
        if (response.result) {
            res.status(200).json(response);
        } else {
            res.status(400).json(response);
            // res.status(400).json({ message: response.error || 'Acto médico no encontrado' });
        }
    }

    async deleteEvento(req: Request, res: Response) {
        const { id } = req.params;
        const response = await EventoService.deleteEvento(+id);
        if (response.result) {
            res.status(200).json(response);
        } else {
            res.status(404).json(response);
            // res.status(404).json({ message: response.error || 'Acto médico no encontrado' });
        }
    }
}

export default new EventoController()